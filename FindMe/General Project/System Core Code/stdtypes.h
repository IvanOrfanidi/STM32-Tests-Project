#ifndef _NOSTD_H
#define _NOSTD_H

typedef signed char s8;   // 8-bit signed integer
typedef signed char i8;   // 8-bit signed integer
typedef unsigned char u8;   // 8-bit signed integer
typedef volatile signed char vi8;   // 8-bit volatile signed integer
typedef volatile unsigned char vu8;   // 8-bit volatile signed integer

typedef short s16;   // 16-bit signed integer
typedef short i16;   // 16-bit signed integer
typedef unsigned short u16;   // 16-bit unsigned integer
typedef volatile short vi16;   // 16-bit volatile signed integer
typedef volatile unsigned short vu16;   // 16-bit volatile unsigned integer

typedef long s32;   // 32-bit signed integer
typedef long i32;   // 32-bit signed integer
typedef unsigned long u32;   // 32-bit unsigned integer
typedef volatile long vi32;   // 32-bit signed integer
typedef volatile unsigned long vu32;   // 32-bit unsigned integer

typedef long long s64;   // 64-bit signed
typedef long long i64;   // 64-bit signed
typedef unsigned long long u64;   // 64-bit unsigned
typedef volatile long long vi64;   // 64-bit signed
typedef volatile unsigned long long vu64;   // 64-bit unsigned

#endif
